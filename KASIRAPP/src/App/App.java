    private void BeditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BeditActionPerformed
        DefaultTableModel model = (DefaultTableModel) Tabel.getModel();
        int rowSelect = Tabel.getSelectedRow();
        
        if (Bedit.getText() == "Edit"){
            Binsert.setEnabled(false);
            Bdelete.setEnabled(false);
            Bedit.setText("Update");
            Tnama.setText(model.getValueAt(rowSelect, 1).toString());
            Tharga.setText(model.getValueAt(rowSelect, 2).toString());
            Tjumlah.setText(model.getValueAt(rowSelect, 3).toString());
        }else{
            Binsert.setEnabled(true);
            Bdelete.setEnabled(true);
            Bedit.setText("Edit");
            
            String namabarang = Tnama.getText();
            String hargabarang = Tharga.getText();
            String jumlahbarang = Tjumlah.getText();
            
            model.setValueAt(namabarang, rowSelect, 1);
            model.setValueAt(hargabarang, rowSelect, 2);
            model.setValueAt(jumlahbarang, rowSelect, 3);
        }
    }//GEN-LAST:event_BeditActionPerformed

    private void BdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BdeleteActionPerformed
        int selectedRow = Tabel.getSelectedRow();
        
        if (selectedRow != -1){
            int id = (int) Tabel.getValueAt(selectedRow, 0);
            dbController.deleteData(id);
            newTable();
        } else {
        JOptionPane.showMessageDialog(null, "Belum Ada Data Terpilih");
        }
    }//GEN-LAST:event_BdeleteActionPerformed

    private void BinsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BinsertActionPerformed
        if (Tnama.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama barang belum diinput");
        }else if(Tharga.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Harga barang belum diinput");
        } else if (Tjumlah.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Jumlah barang belum diinput");
        }else {
            InsertData();
            clear();
            double total = HitungTotal();
            Ttotal.setText("Rp." + String.valueOf(total));
        }
    }//GEN-LAST:event_BinsertActionPerformed

