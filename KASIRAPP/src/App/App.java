    private void newTable(){
        DefaultTableModel model = (DefaultTableModel) Tabel.getModel();
        model.setRowCount(0);

        List<Produk> dataList = dbController.readData();

        for (Produk data : dataList ) {
            Object[] row = {data.getId(), data.getName(), data.getPrice(), data.getStock()};
            model.addRow(row);
        }
    }

